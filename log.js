function rendernut() {
  let html = "";
  // \ html += `<li class="btn-pre"><i class="fa-sharp fa-solid fa-arrow-left"></i></li>
  for (let i = 0; i < sumpage; i++) {
    html += ` <li class="list" onclick="hieuung(${i})">${i + 1}</li>`;
  }
  html += `<li class="btn-next"><i class="fa-sharp fa-solid fa-arrow-right"></i></li>`;
  // console.log(html);
  $(".phantrang ul").append(html);
}
rendernut();

$(".list").eq(0).addClass("acction");
function hieuung(i) {
  $(".list").removeClass("acction");
  currentpage = i + 1;
  star = (currentpage - 1) * numberpage;
  end = currentpage * numberpage;
  renderlist();
  $(".list").eq(i).addClass("acction");
}

$(".btn-pre").on("click", function () {
  currentpage--;
  if (currentpage < 1) {
    currentpage = sumpage;
  }
  $(".list").removeClass("acction");
  $(".list")
    .eq(currentpage - 1)
    .addClass("acction");
  star = (currentpage - 1) * numberpage;
  end = currentpage * numberpage;
  renderlist();
});

$(".btn-next").on("click", function () {
  currentpage++;
  if (currentpage > sumpage) {
    currentpage = 1;
  }
  $(".list").removeClass("acction");
  $(".list")
    .eq(currentpage - 1)
    .addClass("acction");
  star = (currentpage - 1) * numberpage;
  end = currentpage * numberpage;
  renderlist();
});

function myFunction() {
  // Declare variables
  var input, filter, table, tr, td, i, txtValue;
  input = document.getElementById("myInput");
  filter = input.value.toUpperCase();
  table = document.querySelector(".table-loglist");
  tr = table.getElementsByTagName("tr");
  // Loop through all table rows, and hide those who don't match the search query
  for (i = 0; i < tr.length; i++) {
    td = tr[i].getElementsByTagName("td")[0];
    if (td) {
      txtValue = td.textContent || td.innerText;
      if (txtValue.toUpperCase().indexOf(filter) > -1) {
        tr[i].style.display = "";
      } else {
        tr[i].style.display = "none";
      }
    }
  }
}

